#[test]
fn create_engine_returns_valid_handle() {
    let handle = create_engine().expect("create_engine should succeed");
    // handle is a u32 slotmap key; dispose succeeds = valid handle
    dispose_engine(handle).expect("dispose_engine should succeed");
}

#[test]
fn dispose_engine_is_idempotent() {
    let h = create_engine().expect("create_engine should succeed");
    dispose_engine(h).expect("first dispose should succeed");
    dispose_engine(h).expect("second dispose should succeed"); // idempotent
}

// ─── Command execution ────────────────────────────────────────────────────────

#[test]
fn execute_command_add_page_then_vertex() {
    with_engine(|handle| {
        let page_cmd = cmd_add_page();
        execute_command(handle, &page_cmd).expect("add page should succeed");

        // Get the actual page_id from the scene
        let scene_json = get_scene(handle).expect("get_scene should succeed");
        let scene: serde_json::Value =
            serde_json::from_str(&scene_json).expect("scene should be valid JSON");
        let page_id = page_id_from_scene_value(&scene["pages"][0]);

        let vertex_cmd = cmd_add_vertex(page_id, "Node A", 10.0, 20.0, 100.0, 50.0);
        execute_command(handle, &vertex_cmd).expect("add vertex should succeed");

        // Verify scene has the page with display elements
        let scene_json = get_scene(handle).expect("get_scene should succeed");
        let scene: serde_json::Value =
            serde_json::from_str(&scene_json).expect("scene should be valid JSON");
        let pages = scene["pages"].as_array().expect("pages should be array");
        assert_eq!(pages.len(), 1, "should have 1 page");
    });
}

// ─── Undo / Redo ─────────────────────────────────────────────────────────────

#[test]
fn undo_after_add_vertex_restores_empty_model() {
    with_engine(|handle| {
        let page_cmd = cmd_add_page();
        execute_command(handle, &page_cmd).expect("add page should succeed");

        let scene_json = get_scene(handle).expect("get_scene should succeed");
        let scene: serde_json::Value =
            serde_json::from_str(&scene_json).expect("scene should be valid JSON");
        let page_id = page_id_from_scene_value(&scene["pages"][0]);

        let vertex_cmd = cmd_add_vertex(page_id, "Node A", 10.0, 20.0, 100.0, 50.0);
        execute_command(handle, &vertex_cmd).expect("add vertex should succeed");

        assert!(
            engine_can_undo(handle).expect("can_undo should succeed"),
            "can_undo should be true after adding"
        );

        undo(handle).expect("undo should succeed");

        assert!(
            engine_can_undo(handle).expect("can_undo should succeed"),
            "can_undo should be true after undo (page command still in history)"
        );
        assert!(
            engine_can_redo(handle).expect("can_redo should succeed"),
            "can_redo should be true after undo"
        );
    });
}

