//! Native integration tests for diagram-wasm.
//!
//! These tests run with `cargo test --workspace` (native rlib) and do NOT
//! require `wasm32-unknown-unknown`.
//!
//! ## JsValue constraint
//!
//! Functions returning `Result<T, JsValue>` must create a `JsValue` on the
//! error path. `wasm-bindgen`'s `JsValue` is only implemented for wasm32
//! targets; on native, creating a `JsValue` (e.g., via `JsValue::from_str`)
//! panics. Therefore:
//!   - Happy-path tests (functions returning `Ok`) work on native ✓
//!   - Error-path tests (expecting `Err`) require wasm32 — marked
//!     `#[cfg(target_arch = "wasm32")]`
//!
//! ## Test isolation
//!
//! Tests share the process-global `ENGINES` pool (`LazyLock`). Each test
//! creates and disposes its own engine via `with_engine()` to avoid leakage.

use diagram_core::PageId;

use diagram_wasm::{
    create_engine, dispose_engine, engine_can_redo, engine_can_undo, execute_command, get_scene,
    import_drawio, redo, render_pages, render_svg, undo,
};

/// Helper: create a temporary engine, run f, then dispose it.
/// This prevents pool leakage between tests.
fn with_engine<F, R>(f: F) -> R
where
    F: FnOnce(u32) -> R,
{
    let handle = create_engine().expect("create_engine should succeed");
    let result = f(handle);
    dispose_engine(handle).expect("dispose_engine should succeed");
    result
}

/// Build an AddPage command JSON using serde_json::json! macro.
/// The page ID is generated by the engine on apply.
fn cmd_add_page() -> String {
    let cmd = serde_json::json!({
        "AddPage": {
            "page": {
                "id": PageId::default(),
                "name": null,
                "size": {"width": 800.0, "height": 600.0}
            }
        }
    });
    serde_json::to_string(&cmd).unwrap()
}

/// Build an AddVertex command JSON. The vertex is placed on the given page_id.
fn cmd_add_vertex(page_id: PageId, label: &str, x: f64, y: f64, w: f64, h: f64) -> String {
    let cmd = serde_json::json!({
        "AddVertex": {
            "vertex": {
                "geometry": {"x": x, "y": y, "width": w, "height": h, "relative": false},
                "label": {"text": label},
                "page_id": page_id,
                "parent": null,
                "style_id": null
            }
        }
    });
    serde_json::to_string(&cmd).unwrap()
}

/// Reconstruct a PageId from scene JSON's {"idx": u64, "version": u32} representation.
fn page_id_from_scene_value(v: &serde_json::Value) -> PageId {
    serde_json::from_value(v["page_id"].clone()).expect("page_id should deserialize to PageId")
}

