// ─── Import ───────────────────────────────────────────────────────────────────

#[test]
fn import_drawio_replaces_model_atomically() {
    with_engine(|handle| {
        // Pre-populate with commands
        let page_cmd = cmd_add_page();
        execute_command(handle, &page_cmd).expect("add page should succeed");

        let scene_json = get_scene(handle).expect("get_scene should succeed");
        let scene: serde_json::Value =
            serde_json::from_str(&scene_json).expect("scene should be valid JSON");
        let page_id = page_id_from_scene_value(&scene["pages"][0]);

        let v1 = cmd_add_vertex(page_id, "Pre-existing", 0.0, 0.0, 50.0, 50.0);
        execute_command(handle, &v1).expect("add vertex 1 should succeed");

        // Import simple-rect fixture
        let xml = include_str!("../fixtures/simple-rect.drawio");
        import_drawio(handle, xml).expect("import_drawio should succeed");

        // Scene should reflect imported model only
        let scene_json = get_scene(handle).expect("get_scene should succeed");
        let scene: serde_json::Value =
            serde_json::from_str(&scene_json).expect("scene should be valid JSON");
        let pages = scene["pages"].as_array().expect("pages should be array");
        assert_eq!(pages.len(), 1, "simple-rect should have 1 page");

        // Check display_list doesn't contain "Pre-existing" text
        let display_list = &pages[0]["display_list"];
        let arr = display_list
            .as_array()
            .expect("display_list should be array");
        for elem in arr {
            if let Some(text) = elem["text"].as_str() {
                assert!(
                    !text.contains("Pre-existing"),
                    "imported scene should not contain pre-existing vertices, found: {text}"
                );
            }
        }
    });
}

#[test]
fn import_drawio_resets_history() {
    with_engine(|handle| {
        let page_cmd = cmd_add_page();
        execute_command(handle, &page_cmd).expect("add page should succeed");

        assert!(
            engine_can_undo(handle).expect("can_undo should succeed"),
            "can_undo should be true after command"
        );

        let xml = include_str!("../fixtures/simple-rect.drawio");
        import_drawio(handle, xml).expect("import_drawio should succeed");

        assert!(
            !engine_can_undo(handle).expect("can_undo should succeed"),
            "can_undo should be false after import"
        );
        assert!(
            !engine_can_redo(handle).expect("can_redo should succeed"),
            "can_redo should be false after import"
        );
    });
}

// ─── wasm32-only tests ─────────────────────────────────────────────────────

/// JsValue::from_str is only available on wasm32; on native, the error path
/// (pool full) panics when trying to construct the JsValue error.
#[cfg(target_arch = "wasm32")]
#[test]
fn too_many_engines_returns_error() {
    let mut handles: Vec<u32> = Vec::with_capacity(64);
    for _ in 0..64 {
        let h = create_engine().expect("create_engine should succeed");
        handles.push(h);
    }
    let result = create_engine();
    assert!(result.is_err(), "65th engine should return error");
    for h in handles {
        dispose_engine(h).expect("dispose should succeed");
    }
}
