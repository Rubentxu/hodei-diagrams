#[test]
fn undo_with_empty_history_is_noop() {
    with_engine(|handle| {
        assert!(
            !engine_can_undo(handle).expect("can_undo should succeed"),
            "can_undo should be false on fresh engine"
        );
        undo(handle).expect("undo on empty history should be no-op");
        assert!(
            !engine_can_undo(handle).expect("can_undo should succeed"),
            "can_undo should still be false"
        );
    });
}

#[test]
fn new_command_clears_redo_history() {
    with_engine(|handle| {
        let page_cmd = cmd_add_page();
        execute_command(handle, &page_cmd).expect("add page should succeed");

        let scene_json = get_scene(handle).expect("get_scene should succeed");
        let scene: serde_json::Value =
            serde_json::from_str(&scene_json).expect("scene should be valid JSON");
        let page_id = page_id_from_scene_value(&scene["pages"][0]);

        let v1 = cmd_add_vertex(page_id, "Node A", 10.0, 20.0, 100.0, 50.0);
        execute_command(handle, &v1).expect("add vertex 1 should succeed");

        undo(handle).expect("undo should succeed");
        assert!(
            engine_can_redo(handle).expect("can_redo should succeed"),
            "can_redo should be true after undo"
        );

        let v2 = cmd_add_vertex(page_id, "Node B", 30.0, 40.0, 80.0, 30.0);
        execute_command(handle, &v2).expect("add second vertex should succeed");

        assert!(
            !engine_can_redo(handle).expect("can_redo should succeed"),
            "can_redo should be false after new command"
        );
    });
}

